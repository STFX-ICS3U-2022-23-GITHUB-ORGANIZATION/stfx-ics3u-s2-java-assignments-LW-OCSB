package lessons;

public class ConditionsP4 {

	public static void main(String[] args) {
		
		// Comparison operators:
		// >, <, <=, >=, ==, !=
		
		// Greater than
		if (13 > 17)  {
			// Code block executed if true
			System.out.println("Hello World!");
		}
		
		// Less than
		if (13 < 17)  {
			// Code block executed if true
			System.out.println("Hello World!");
		}
		
		// Greater than or equal
		if (13 >= 13)  {
			// Code block executed if true
			System.out.println("Hello World!");
		}
		
		// Equals
		if (13 == 13)  {
			// Code block executed if true
			System.out.println("Goodbye World!");
		}
		
		// Equals
		if (13 != 17)  {
			// Code block executed if true
			System.out.println("Goodbye World Again!");
		}
		
		
	}

}
