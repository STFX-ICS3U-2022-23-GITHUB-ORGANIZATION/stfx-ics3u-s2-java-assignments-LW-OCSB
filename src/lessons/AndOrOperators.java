package lessons;

public class AndOrOperators {

	public static void main(String[] args) {

		// &&  - and
		// || - or 

		
		// both conditions must be true for whole condition to be true
		if ( (2 > 17)  &&  (3 > 2) )  {
			
			System.out.println("Hello World!");
			
		}
		
		// one condition must be true for whole condition to be true
		if ( (2 > 17)  ||  (3 > 2) )  {
					
			System.out.println("Hello World Again!");
		}
		
		
	}

}
